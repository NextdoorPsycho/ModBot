package com.volmit.modbot.adapters.slash.project.json;

public class Settings {
    private String colorcode;
    private String downloadable;
    private String someOtherSetting;

    public String getColorcode() {
        return colorcode;
    }

    public void setColorcode(String colorcode) {
        this.colorcode = colorcode;
    }

    public String getDownloadable() {
        return downloadable;
    }

    public void setDownloadable(String downloadable) {
        this.downloadable = downloadable;
    }

    public String getSomeOtherSetting() {
        return someOtherSetting;
    }

    public void setSomeOtherSetting(String someOtherSetting) {
        this.someOtherSetting = someOtherSetting;
    }
}